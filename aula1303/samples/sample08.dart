/**
 * Este código define uma função principal que recebe uma 
 * lista de strings como argumentos. Em seguida, declara uma 
 * variável soma que é uma função que recebe dois inteiros e 
 * retorna um inteiro. Atribui a função somarFn à variável 
 * soma e imprime o resultado de chamar 
 * soma com os argumentos 2 e 3.
 */
void main(List<String> args) {
  int Function(int, int) soma = somarFn;
  print(soma(2, 3));
}

